	mapIcon:string#场景图标
	pkModel:int#地图模式
1：和平地图
2：安全地图（白灰名不受pk）
3: pk地图
4：切磋地图（pk不掉落）
	openLevel:int#开启等级
	maxCount:int#进入次数最大上限
0无限进入
	playernum:int#进入人数上限
	lifeTime:int#存在时间
毫秒
0为不销毁
	waitingTime:int#销毁等待时间
毫秒
	bossName:string#副本怪物名字
	sound:string#音乐
	expendStr:int[]#扩展字段
43：副本开启消耗
0：副本难度
	rewardType:int#副本奖励类型
(和instanceReward里面的奖励类型对应)
	relive:int#副本复活类型
0：场景出生点复活
1：回城复活
2：消耗物品复活
3：大荒塔复活（掉2层）
4：竞技场复活（死后出结算界面）
5：城战复活（不能用道具，倒计时5秒自动复活）
	reward:int[]#奖励预览
1=装备 2=物品 3=金币 4=钻石 5=代金卷 6=贡献值 7=荣誉值 8=经验 9=宝玉1 0=翅膀 11=时装
【类型，id，数量，概率】
	ability:int#怪物战力
副本显示用
	lv:int#副本难度分类
1：单人
2：组队
3：炼狱
	BOSSFocus:int#是否聚焦BOSS
	nineGrid:int#是否开启九宫格
1：开启
]]

